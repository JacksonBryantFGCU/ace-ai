import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";

/**
 * Harvest the real React (+ csstype) type declarations into a bundled module so
 * Monaco's TypeScript language service has a virtual `node_modules` — the same
 * type universe `tsc` sees. Run via `pnpm generate:monaco-libs` when the React
 * types version changes.
 *
 * Output: `lib/monaco/generated-libs.ts` exporting `{ path, content }[]`, where
 * `path` is the vFS path (e.g. `node_modules/@types/react/index.d.ts`). The
 * content is embedded as a JSON string parsed at runtime, so TypeScript doesn't
 * infer a giant literal type for it.
 */

const require = createRequire(import.meta.url);
const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");

const reactPkgPath = require.resolve("@types/react/package.json");
const reactDir = dirname(reactPkgPath);

// Resolve csstype from @types/react's own dependency tree (pnpm-nested).
const reqFromReact = createRequire(reactPkgPath);
const csstypePkgPath = reqFromReact.resolve("csstype/package.json");
const csstypeDir = dirname(csstypePkgPath);

/** @type {{ path: string; content: string }[]} */
const libs = [];
const add = (vfsPath, absPath) => libs.push({ path: vfsPath, content: readFileSync(absPath, "utf8") });

// Register the @types/react declarations AS the `react` package (node_modules/
// react/...), so `react` and its subpaths (`react/jsx-runtime`, etc.) resolve by
// direct module resolution — no fragile @types subpath handling, which fails
// under classic node resolution in both Monaco and a bare compiler host.
const reactDts = [];
for (const file of readdirSync(reactDir)) {
  if (file.endsWith(".d.ts")) {
    add(`node_modules/react/${file}`, join(reactDir, file));
    reactDts.push(file);
  }
}
// Synthetic package.json: `types` for classic resolution + `exports` for bundler.
const exportsMap = { ".": { types: "./index.d.ts" } };
for (const file of reactDts) {
  if (file === "index.d.ts") continue;
  exportsMap[`./${file.replace(/\.d\.ts$/, "")}`] = { types: `./${file}` };
}
libs.push({
  path: "node_modules/react/package.json",
  content: JSON.stringify({ name: "react", version: "19.0.0", types: "index.d.ts", exports: exportsMap }),
});

// csstype (referenced by the React types for CSSProperties).
add("node_modules/csstype/index.d.ts", join(csstypeDir, "index.d.ts"));
libs.push({
  path: "node_modules/csstype/package.json",
  content: JSON.stringify({ name: "csstype", version: "3.0.0", types: "index.d.ts" }),
});

const json = JSON.stringify(libs);
const body = `/* eslint-disable */
// GENERATED by scripts/generate-monaco-libs.mjs — DO NOT EDIT.
// React + csstype type declarations, embedded for Monaco's virtual node_modules.
export interface MonacoLib { path: string; content: string }
// prettier-ignore
export const MONACO_LIBS: MonacoLib[] = JSON.parse(${JSON.stringify(json)});
`;

writeFileSync(join(ROOT, "lib", "monaco", "generated-libs.ts"), body, "utf8");
console.log(`Wrote ${libs.length} libs (${(json.length / 1024).toFixed(0)} KB) to lib/monaco/generated-libs.ts`);
